     */
    public function store(Request $request)
    {
        $request->validate([
            'hotel_name' => 'required|max:255|unique:hotels',
        ],[
           'hotel_name.required' => 'Name of Hotel is Required',
           'hotel_name.unique' => 'Hotel is Already Added', 
        ]);

        if ($request) {
            $save = new Hotel;
            $save->hotel_name = $request->hotel_name;
            $save->save();
        }
        return redirect()->route('Hotel.create')->with('success', 'Add Success');
    }

